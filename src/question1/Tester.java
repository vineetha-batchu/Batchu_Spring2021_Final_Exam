public abstract class Tester implements ItIndustry
{
    private String fname,lname;

    public Tester(String fname, String lname) {
        this.fname = fname;
        this.lname = lname;
    }

    public String getFname() {
        return fname;
    }

    public void setFname(String fname) {
        this.fname = fname;
    }

    public String getLname() {
        return lname;
    }

    public void setLname(String lname) {
        this.lname = lname;
    }

    @Override
    public String toString() {
        return "Tester{" + "fname=" + fname + ", lname=" + lname + '}';
    }
    
    public abstract void testingType();
}
